        this.businesscreationdoes = businesscreationdoes;
        this.businessdeadlinedoes = businessdeadlinedoes;
        this.businessassignedto = businessassignedto;
        this.businessstatus = businessstatus;
        this.businessassignedtochange = businessassignedtochange;
        this.businessstatuschange = businessstatuschange;
        this.businesskeyvalue = businesskeyvalue;
        this.teamname = teamname;
