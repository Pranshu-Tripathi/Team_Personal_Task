    public String getBusinesstitledoes() {
        return businesstitledoes;
    }

    public void setBusinesstitledoes(String businesstitledoes) {
        this.businesstitledoes = businesstitledoes;
    }

    public String getBusinessdescriptiondoes() {
        return businessdescriptiondoes;
    }

    public void setBusinessdescriptiondoes(String businessdescriptiondoes) {
        this.businessdescriptiondoes = businessdescriptiondoes;
    }

    public String getBusinesscreationdoes() {
        return businesscreationdoes;
    }

    public void setBusinesscreationdoes(String businesscreationdoes) {
        this.businesscreationdoes = businesscreationdoes;
    }

    public String getBusinessdeadlinedoes() {
        return businessdeadlinedoes;
    }

    public void setBusinessdeadlinedoes(String businessdeadlinedoes) {
        this.businessdeadlinedoes = businessdeadlinedoes;
    }

    public String getBusinessassignedto() {
        return businessassignedto;
    }

    public void setBusinessassignedto(String businessassignedto) {
        this.businessassignedto = businessassignedto;
    }

    public String getBusinessstatus() {
        return businessstatus;
    }

    public void setBusinessstatus(String businessstatus) {
        this.businessstatus = businessstatus;
    }

    public String getBusinessassignedtochange() {
        return businessassignedtochange;
    }

    public void setBusinessassignedtochange(String businessassignedtochange) {
        this.businessassignedtochange = businessassignedtochange;
    }

    public String getBusinessstatuschange() {
        return businessstatuschange;
    }

    public void setBusinessstatuschange(String businessstatuschange) {
        this.businessstatuschange = businessstatuschange;
    }

    public String getBusinesskeyvalue() {
        return businesskeyvalue;
    }

    public void setBusinesskeyvalue(String businesskeyvalue) {
        this.businesskeyvalue = businesskeyvalue;
    }

    public String getTeamname() {
        return teamname;
    }

    public void setTeamname(String teamname) {
        this.teamname = teamname;
    }
